	CFIntTopDomainBuff::CFIntTopDomainBuff()
	: cflib::ICFLibCloneableObj()
	{
		requiredRevision = 1;
		for( int i = 0; i < 16; i++ ) {
			createdByUserId[i] = 0;
		}
		createdAt = cflib::CFLib::getUTCTimestampInstance();
		for( int i = 0; i < 16; i++ ) {
			updatedByUserId[i] = 0;
		}
		updatedAt = cflib::CFLib::getUTCTimestampInstance();
		requiredTenantId = cfint::CFIntTopDomainBuff::TENANTID_INIT_VALUE;
		requiredId = cfint::CFIntTopDomainBuff::ID_INIT_VALUE;
		requiredTldId = cfint::CFIntTopDomainBuff::TLDID_INIT_VALUE;
		requiredName = new std::string( cfint::CFIntTopDomainBuff::NAME_INIT_VALUE );
		optionalDescription = NULL;
	}

