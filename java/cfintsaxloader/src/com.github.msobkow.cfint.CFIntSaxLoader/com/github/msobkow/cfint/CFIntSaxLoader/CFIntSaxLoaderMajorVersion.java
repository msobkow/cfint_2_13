/*
 *	CFIntSaxLoaderMajorVersionParse XML SAX Element Handler implementation
 *	for MajorVersion.
 */
public class CFIntSaxLoaderMajorVersion
	extends CFLibXmlCoreElementHandler
{
	public CFIntSaxLoaderMajorVersion( CFIntSaxLoader saxLoader ) {
		super( saxLoader );
	}

	public void startElement(
		String		uri,
		String		localName,
		String		qName,
		Attributes	attrs )
	throws SAXException
	{
		final String S_ProcName = "startElement";
		ICFIntMajorVersionObj origBuff = null;
		ICFIntMajorVersionEditObj editBuff = null;
		// Common XML Attributes
		String attrId = null;
		// MajorVersion Attributes
		String attrName = null;
		String attrDescription = null;
		// MajorVersion References
		ICFIntTenantObj refTenant = null;
		ICFIntSubProjectObj refParentSPrj = null;
		// Attribute Extraction
		String attrLocalName;
		int numAttrs;
		int idxAttr;
		final String S_LocalName = "LocalName";
		try {
			assert qName.equals( "MajorVersion" );

			CFIntSaxLoader saxLoader = (CFIntSaxLoader)getParser();
			if( saxLoader == null ) {
				throw new CFLibNullArgumentException( getClass(),
					S_ProcName,
					0,
					"getParser()" );
			}

			ICFIntSchemaObj schemaObj = saxLoader.getSchemaObj();
			if( schemaObj == null ) {
				throw new CFLibNullArgumentException( getClass(),
					S_ProcName,
					0,
					"getParser().getSchemaObj()" );
			}

			// Instantiate an edit buffer for the parsed information
			origBuff = (ICFIntMajorVersionObj)schemaObj.getMajorVersionTableObj().newInstance();
			editBuff = (ICFIntMajorVersionEditObj)origBuff.beginEdit();

			// Extract Attributes
			numAttrs = attrs.getLength();
			for( idxAttr = 0; idxAttr < numAttrs; idxAttr++ ) {
				attrLocalName = attrs.getLocalName( idxAttr );
				if( attrLocalName.equals( "Id" ) ) {
					if( attrId != null ) {
						throw new CFLibUniqueIndexViolationException( getClass(),
							S_ProcName,
							S_LocalName,
							attrLocalName );
					}
					attrId = attrs.getValue( idxAttr );
				}
				else if( attrLocalName.equals( "Name" ) ) {
					if( attrName != null ) {
						throw new CFLibUniqueIndexViolationException( getClass(),
							S_ProcName,
							S_LocalName,
							attrLocalName );
					}
					attrName = attrs.getValue( idxAttr );
				}
				else if( attrLocalName.equals( "Description" ) ) {
					if( attrDescription != null ) {
						throw new CFLibUniqueIndexViolationException( getClass(),
							S_ProcName,
							S_LocalName,
							attrLocalName );
					}
					attrDescription = attrs.getValue( idxAttr );
				}
				else if( attrLocalName.equals( "schemaLocation" ) ) {
					// ignored
				}
				else {
					throw new CFLibUnrecognizedAttributeException( getClass(),
						S_ProcName,
						getParser().getLocationInfo(),
						attrLocalName );
				}
			}

			// Ensure that required attributes have values
			if( attrName == null ) {
				throw new CFLibNullArgumentException( getClass(),
					S_ProcName,
					0,
					"Name" );
			}

			// Save named attributes to context
			CFLibXmlCoreContext curContext = getParser().getCurContext();
			curContext.putNamedValue( "Id", attrId );
			curContext.putNamedValue( "Name", attrName );
			curContext.putNamedValue( "Description", attrDescription );

			// Convert string attributes to native Java types
			// and apply the converted attributes to the editBuff.

			Integer natId;
			if( ( attrId != null ) && ( attrId.length() > 0 ) ) {
				natId = Integer.valueOf( Integer.parseInt( attrId ) );
			}
			else {
				natId = null;
			}
			String natName = attrName;
			editBuff.setRequiredName( natName );

			String natDescription = attrDescription;
			editBuff.setOptionalDescription( natDescription );

			// Get the scope/container object

			CFLibXmlCoreContext parentContext = curContext.getPrevContext();
			Object scopeObj;
			if( parentContext != null ) {
				scopeObj = parentContext.getNamedValue( "Object" );
			}
			else {
				scopeObj = null;
			}

			// Resolve and apply required Container reference

			if( scopeObj == null ) {
				throw new CFLibNullArgumentException( getClass(),
					S_ProcName,
					0,
					"scopeObj" );
			}
			else if( scopeObj instanceof ICFIntSubProjectObj ) {
				refParentSPrj = (ICFIntSubProjectObj) scopeObj;
				editBuff.setRequiredContainerParentSPrj( refParentSPrj );
				refTenant = (ICFIntTenantObj)editBuff.getRequiredOwnerTenant();
			}
			else {
				throw new CFLibUnsupportedClassException( getClass(),
					S_ProcName,
					"scopeObj",
					scopeObj,
					"ICFIntSubProjectObj" );
			}

			// Resolve and apply Owner reference

			if( refTenant == null ) {
				if( scopeObj instanceof ICFIntTenantObj ) {
					refTenant = (ICFIntTenantObj) scopeObj;
					editBuff.setRequiredOwnerTenant( refTenant );
				}
				else {
					throw new CFLibNullArgumentException( getClass(),
						S_ProcName,
						0,
						"Owner<Tenant>" );
				}
			}

			CFIntSaxLoader.LoaderBehaviourEnum loaderBehaviour = saxLoader.getMajorVersionLoaderBehaviour();
			ICFIntMajorVersionEditObj editMajorVersion = null;
			ICFIntMajorVersionObj origMajorVersion = (ICFIntMajorVersionObj)schemaObj.getMajorVersionTableObj().readMajorVersionByNameIdx( refTenant.getRequiredId(),
			refParentSPrj.getRequiredId(),
			editBuff.getRequiredName() );
			if( origMajorVersion == null ) {
				editMajorVersion = editBuff;
			}
			else {
				switch( loaderBehaviour ) {
					case Insert:
						break;
					case Update:
						editMajorVersion = (ICFIntMajorVersionEditObj)origMajorVersion.beginEdit();
						editMajorVersion.setRequiredName( editBuff.getRequiredName() );
						editMajorVersion.setOptionalDescription( editBuff.getOptionalDescription() );
						break;
					case Replace:
						editMajorVersion = (ICFIntMajorVersionEditObj)origMajorVersion.beginEdit();
						editMajorVersion.deleteInstance();
						editMajorVersion = null;
						origMajorVersion = null;
						editMajorVersion = editBuff;
						break;
				}
			}

			if( editMajorVersion != null ) {
				if( origMajorVersion != null ) {
					editMajorVersion.update();
				}
				else {
					origMajorVersion = (ICFIntMajorVersionObj)editMajorVersion.create();
				}
				editMajorVersion = null;
			}

			curContext.putNamedValue( "Object", origMajorVersion );
		}
		catch( RuntimeException e ) {
			throw new SAXException( "Near " + getParser().getLocationInfo() + ": Caught and rethrew " + e.getClass().getName() + " - " + e.getMessage(),
				e );
		}
		catch( Error e ) {
			throw new SAXException( "Near " + getParser().getLocationInfo() + ": Caught and rethrew " + e.getClass().getName() + " - " + e.getMessage() );
		}
	}

	public void endElement(
		String		uri,
		String		localName,
		String		qName )
	throws SAXException
	{
	}
}
